function show0()
{
  document.getElementsByClassName("name")[0].style.display="block"
var audio=new Audio("zero.mp3")
audio.play()


  document.getElementsByTagName("button")[0].classList.add("press")

  setTimeout(function(){

    document.getElementsByTagName("button")[0].classList.remove("press");
  }, 175);
}



